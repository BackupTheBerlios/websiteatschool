/** check an arbitrary string for UTF-8 conformity
 *
 * return TRUE if string is valid UTF-8, FALSE otherwise.
 *
 * @param string $str the string to check
 * @return bool TRUE if valid UTF-8, FALSE otherwise
 */
function utf8_validate($str) {
    $pattern = '/^([\\x00-\\x7F]'.                         // ASCII (including ctrl-chars)
               '|[\\xC2-\\xDF][\\x80-\\xBF]'.              // non-overlong 2-byte
               '|\\xE0[\\xA0-\\xBF][\\x80-\\xBF]'.         // 3-byte excluding overlongs
               '|[\\xE1-\\xEC\\xEE\\xEF][\\x80-\\xBF]{2}'. // 3-byte (plain)
               '|\\xED[\\x80-\\x9F][\\x80-\\xBF]'.         // 3-byte excluding surrogates
               '|\\xF0[\\x90-\\xBF][\\x80-\\xBF]{2}'.      // 4-byte excluding overlongs
               '|[\\xF1-\\xF3][\\x80-\\xBF]{3}'.           // 4-byte planes 4-15
               '|\\xF4[\\x80-\\x8F][\\x80-\\xBF]{2})*$/';  // 4-byte plane 16
    return (preg_match($pattern,$str) === 1);
} // utf8_validate()


/** calculate the number of code points encoded in an UTF-8 string
 *
 * This routine uses a trick to calculate the number of code points
 * available in string $str. By first converting the UTF-8 string to ISO-8859-1
 * all multi-byte code points (ie. all non-ASCII) are converted to a single
 * byte: the correct ISO-8859-1 character where possible and a '?' for
 * characters not available in the ISO-8859-1 reportoire.
 * Note that utf8_decode() does NOT work very well on ill-formed UTF-8 strings,
 * e.g. it happily interprets codes in invalid planes and translates truncated
 * sequences to chr(0). The good news is that it works for valid UTF-8.
 *
 * @param string $utf8str a valid UTF-8 string to examine
 * @return int the number of code points in string $str
 */
function utf8_strlen($utf8str) {
    return strlen(utf8_decode($utf8str));
} // utf8_strlen()


/** fold a UTF-8 string to lower case
 *
 * this routine tries to use the multibyte routine for folding,
 * but if it is not available we fall back to our own translation
 * table $UTF8_UPPER_LOWER which is derived straight from the
 * Unicode Character Database.
 *
 * Informal benchmarking yielded no significant speed difference
 * between mb_strtolower() and strtr(). However, our table needs
 * memory to store the 1100+ pairs and for mb_strtolower() this is
 * already taken care of. OTOH: depending on the version of mb_string,
 * our table (UCD 6.0.0 February 2011) may be more complete and up
 * to date. Oh well.
 *
 * @param string $utf8str a valid UTF-8 string to examine
 * @return string the lowercase equivalent of $utf8str
 */
function utf8_strtolower($utf8str) {
    if (USE_MBSTRING) {
        return mb_strtolower($utf8str,'UTF-8');
    } else {
        global $UTF8_UPPER_LOWER;
        return strtr($utf8str,$UTF8_UPPER_LOWER);
    }
} // utf8_strtolower()


/** fold a UTF-8 string to upper case (sort of)
 *
 * this routine tries to use the multibyte routine for folding,
 * but if it is not available we fall back to our own translation
 * table $UTF8_LOWER_UPPER which is derived from the
 * Unicode Character Database. Note that this table has some
 * quirks because the underlying table is injective.
 *
 * @param string $utf8str a valid UTF-8 string to examine
 * @return string the uppercase equivalent of $utf8str
 */
function utf8_strtoupper($utf8str) {
    if (USE_MBSTRING) {
        return mb_strtoupper($utf8str,'UTF-8');
    } else {
        global $UTF8_LOWER_UPPER;
        return strtr($utf8str,$UTF8_LOWER_UPPER);
    }
} // utf8_strtoupper()


/** compare two UTF8 strings in a case-INsensitive way
 *
 * This compares two UTF-8 strings caseINsensitive. We
 * do this by comparing the lowercase variant of the strings
 * with the regular strcmp. We use lowercasing because that
 * translation table is 'better' than the upper case one.
 *
 * Note that this is a quick and dirty approach: we simply
 * use the multibyte strings as-is rather than extracting
 * the actual code points and applying some sort of collation
 * because that would make it much more complicated.
 *
 * @param string $utf8str1 first string
 * @param string $utf8str2 second string
 * @return int result < 0 if string 1 < string 2, result > 0 if string 1 > string 2, 0 when equal
 */
function utf8_strcasecmp($utf8str1,$utf8str2) {
    return strcmp(utf8_strtolower($utf8str1),utf8_strtolower($utf8str2));
} // utf8_strcasecmp()


/** return part of a UTF-8 string
 *
 * this routine returns a valid UTF-8 substring from $utf8str
 * based on the $start and $length parameters in a way comparable
 * to substr(). If available, we use the mb_substr() replacement,
 * otherwise we perform the requested actions ourselves.
 *
 * If we do it ourselves, we prefix variable names with 'c_' for characters
 * and 'b_' for bytes. A UTF-8 character takes up to 4 bytes.
 *
 * @param string $utf8str a valid UTF-8 string to examine
 * @param int $start an offset expressed in characters (not bytes)
 * @param int $length the length of the string to return (also expressed in characters)
 * @return string the requested substring of $utf8str or FALSE if $start points beyond the string
 */
function utf8_substr($utf8str,$start,$length=NULL) {
    if (USE_MBSTRING) {
        return mb_substr($utf8str,$start,(is_null($length)) ? utf8_strlen($utf8str) : $length,'UTF-8');
    }

    $c_strlen = utf8_strlen($utf8str);
    if ($start >= $c_strlen) {
        return FALSE;
    }
    // convert the negative offset from end of string into a non-negative offset from start of string
    $c_start = ($start < 0) ? max($c_strlen + $start, 0) : $start;

    // calculate the byte offset equivalent with the requested character offset
    $b_start = 0;     
    for ($c_index = 0; $c_index < $c_start; ++$c_index) {
        $byte = ord($utf8str[$b_start++]);
        if (($byte & 0x80) == 0x00) {       // 0xxx.xxxx => plain ascii; we're done here
            continue;
        } elseif (($byte & 0xE0) == 0xC0) { // 110x.xxxx => start of 2-byte sequence
            ++$b_start;
        } elseif (($byte & 0xF0) == 0xE0) { // 1110.xxxx => start of 3-byte sequence
            $b_start += 2;
        } elseif (($byte & 0xF8) == 0xF0) { // 1111.0xxx => start of 4-byte sequence
            $b_start += 3;
        } // else invalid byte shouldn't happen with valid UTF-8
    }
    // If no length was specified, they want the part from $b_start to end of string so we're done here
    if (is_null($length)) {
        return substr($utf8str,$b_start);
    }
    // convert the negative length into a non-negative offset from start of string
    $c_end = ($length < 0) ? max($c_strlen + $length,0) : min($c_start + $length,$c_strlen);

    // continue counting characters from where we were
    for ($b_end = $b_start; $c_index < $c_end; ++$c_index) {
        $byte = ord($utf8str[$b_end++]);
        if (($byte & 0x80) == 0x00) {       // 0xxx.xxxx => plain ascii; we're done here
            continue;
        } elseif (($byte & 0xE0) == 0xC0) { // 110x.xxxx => start of 2-byte sequence
            ++$b_end;
        } elseif (($byte & 0xF0) == 0xE0) { // 1110.xxxx => start of 3-byte sequence
            $b_end += 2;
        } elseif (($byte & 0xF8) == 0xF0) { // 1111.0xxx => start of 4-byte sequence
            $b_end += 3;
        } // else invalid byte (shouldn't happen with valid UTF-8)
    }
    return substr($utf8str,$b_start,$b_end-$b_start);
} // utf8_substr()


/** map some UTF-8 characters to comparable ASCII strings
 *
 * this maps a lot of UTF-8 characters to more or less comparable
 * ASCII strings, e.g. an A-acute is mapped to 'A', etc.
 * Handy when trying to construct readable filenames from
 * letters with diacritics, eg. e-acute 'l' e-grave 'v' 'e' (French
 * for pupil) maps to 'e' 'l' 'e' 'v' e' rather than 'l' 'v' 'e'
 * if the diacriticals would simply be 'eaten'.
 *
 * Note that UTF-8 characters that are NOT mapped to ASCII are
 * retained, i.e. the result is not plain ASCII but an UTF-8 string
 * with as much characters mapped to ASCII as possible.
 *
 * @param string $utf8str input string possibly with letters with diacriticals etc.
 * @return string a 'best-effort' ASCII-approximation of $utf8str
 * @uses $UTF8_ASCII
 */
function utf8_strtoascii($utf8str) {
    global $UTF8_ASCII;
    return strtr($utf8str,$UTF8_ASCII);
} // utf8_strtoascii()


